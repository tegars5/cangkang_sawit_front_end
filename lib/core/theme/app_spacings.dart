/// Centralized spacing constants for consistent padding and margins
class AppSpacings {
  AppSpacings._(); // Private constructor to prevent instantiation

  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;

  // Specific use cases
  static const double cardPadding = xl; // 32
  static const double screenPadding = lg; // 24
  static const double sectionSpacing = lg; // 24
  static const double itemSpacing = md; // 16
  static const double smallSpacing = sm; // 8
}
